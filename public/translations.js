/**
 * Handles full client-side translation of a page.
 * @param {string} langCode - Language code ('ar', 'en', 'fr', 'es', 'zh', 'ru')
 */
window.changeLanguage = function(langCode) {
  const html = document.documentElement;
  const currentLang = html.getAttribute('lang') || 'ar';
  const newDir = langCode === 'ar' ? 'rtl' : 'ltr';

  html.setAttribute('lang', langCode);
  html.setAttribute('dir', newDir);
  localStorage.setItem('preferredLang', langCode);

  // Update Toggle Button visual text (e.g. "EN", "FR" or "عربي")
  const toggleText = document.getElementById('langToggleText');
  const loginToggleText = document.getElementById('loginLangText');
  if (toggleText) {
    toggleText.innerText = langCode === 'ar' ? 'EN' : langCode.toUpperCase();
  }
  if (loginToggleText) {
    loginToggleText.innerText = langCode === 'ar' ? 'EN' : langCode.toUpperCase();
  }

  // Adjust responsive layouts for RTL/LTR compatibility dynamically
  adjustLayoutDirection(langCode);

  // Iterate over all tags with .lang-text class and update content
  document.querySelectorAll('.lang-text').forEach(el => {
    const tagName = el.tagName.toLowerCase();
    
    // Attempt dictionary translation first using the data-ar key or text content
    let key = el.getAttribute('data-ar') || el.innerText.trim();
    if (!key && el.hasAttribute('placeholder')) {
      key = el.getAttribute('data-ar-placeholder');
    }

    if (key && translationDict[key] && translationDict[key][langCode]) {
      // Translated text is in dictionary!
      if (tagName === 'input') {
        if (el.hasAttribute('placeholder')) {
          el.setAttribute('placeholder', translationDict[key][langCode]);
        } else {
          el.value = translationDict[key][langCode];
        }
      } else {
        el.innerHTML = translationDict[key][langCode];
      }
    } else {
      // Fallback: Check standard static attributes if present (e.g., data-en)
      if (tagName === 'input') {
        if (el.hasAttribute('placeholder')) {
          const attrVal = el.getAttribute(`data-${langCode}`) || el.getAttribute('data-en') || el.getAttribute('data-ar');
          if (attrVal) el.setAttribute('placeholder', attrVal);
        }
      } else {
        const attrVal = el.getAttribute(`data-${langCode}`) || el.getAttribute('data-en') || el.getAttribute('data-ar');
        if (attrVal) el.innerHTML = attrVal;
      }
    }
  });

  // Execute specific adjustment callbacks if defined by individual page scripts
  if (typeof window.adjustHeaderPosition === 'function') {
    window.adjustHeaderPosition();
  }
  if (typeof renderShipments === 'function') {
    renderShipments();
  }
  if (typeof renderWarehouseSliders === 'function') {
    renderWarehouseSliders();
  }
  if (typeof renderClientsTable === 'function') {
    renderClientsTable();
  }
  if (typeof renderLiveLogsFeed === 'function') {
    renderLiveLogsFeed();
  }
};

/**
 * Handle structural shifts (sidebars, absolute header margins, back button icons)
 */
function adjustLayoutDirection(lang) {
  const isRtl = lang === 'ar';
  
  // Flip SideNav aside if it exists
  const sidebar = document.querySelector('aside');
  const main = document.querySelector('main');
  if (sidebar && main) {
    if (isRtl) {
      sidebar.classList.remove('left-0');
      sidebar.classList.add('right-0');
      main.classList.remove('pl-0', 'md:pl-64');
      main.classList.add('pr-0', 'md:pr-64');
    } else {
      sidebar.classList.remove('right-0');
      sidebar.classList.add('left-0');
      main.classList.remove('pr-0', 'md:pr-64');
      main.classList.add('pl-0', 'md:pl-64');
    }
  }

  // Flip Back Button Arrow Icon on Login Page
  const backIcon = document.getElementById('back-btn-icon');
  if (backIcon) {
    backIcon.innerText = isRtl ? 'arrow_forward' : 'arrow_back';
  }

  // Handle CSS variable rules or dynamic dropdown updates
  const dropdown = document.querySelector('.group.relative .absolute');
  if (dropdown) {
    if (isRtl) {
      dropdown.classList.remove('right-0');
      dropdown.classList.add('left-0');
    } else {
      dropdown.classList.remove('left-0');
      dropdown.classList.add('right-0');
    }
  }
}

